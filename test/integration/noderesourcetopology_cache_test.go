type podDesc struct {
	schedulerName string
	podName       string
	isGuaranteed  bool
	isDelete      bool
	resourcesMap  map[string]string
	expectedNode  string
	// autogenerated
	pod *corev1.Pod
}

func (p *podDesc) SetupPod(ns string, initContainer bool) {
	pt := st.MakePod().Namespace(ns).Name(p.podName).SchedulerName(p.schedulerName)
	if p.isGuaranteed {
		p.pod = util.WithLimits(pt, p.resourcesMap, initContainer).Obj()
	} else {
		p.pod = util.WithRequests(pt, p.resourcesMap, initContainer).Obj()
	}
}

